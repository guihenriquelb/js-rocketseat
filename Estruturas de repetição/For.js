//for


for(let i = 0; i < 10; i++){
    console.log(i)
}


// for (break - para a execução do loop)

for(let i = 1; i <= 10; i++){
    if (i === 5){
        break;
    }
    console.log(i)
}

// for (continue - pula a execução do momento)

for(let i = 1; i <= 10; i++){
    if (i === 5){
        continue;
    }
    console.log(i)
}